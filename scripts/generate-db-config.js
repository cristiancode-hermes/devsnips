/**
 * Build-time script: reads NEON_API_KEY (from env or .neon-api-key),
 * fetches the DB password from Neon's API, and writes _db-config.js
 * (underscore prefix so Netlify doesn't treat it as a separate function).
 *
 * Uses node:https instead of fetch for Netlify build environment compat.
 *
 * Usage: node scripts/generate-db-config.js
 * ENV: NEON_API_KEY
 */

const fs = require('fs');
const path = require('path');
const https = require('https');

const ROLE_NAME = 'neondb_owner';
const HOST = 'ep-old-star-abhpdbpp-pooler.eu-west-2.aws.neon.tech';

function httpsGet(url, apiKey) {
  return new Promise((resolve, reject) => {
    const u = new URL(url);
    const opts = {
      hostname: u.hostname,
      port: 443,
      path: u.pathname + u.search,
      method: 'GET',
      headers: {
        'Authorization': 'Bearer ' + apiKey,
        'Accept': 'application/json',
      },
      rejectUnauthorized: true,
    };
    const req = https.request(opts, (res) => {
      let data = '';
      res.on('data', (chunk) => { data += chunk; });
      res.on('end', () => {
        if (res.statusCode >= 200 && res.statusCode < 300) {
          try { resolve(JSON.parse(data)); }
          catch (e) { reject(new Error('JSON parse error: ' + e.message + ' - data: ' + data.slice(0,200))); }
        } else {
          reject(new Error('HTTP ' + res.statusCode + ' - ' + data.slice(0,300)));
        }
      });
    });
    req.on('error', reject);
    req.setTimeout(20000, () => { req.destroy(); reject(new Error('Request timeout')); });
    req.end();
  });
}

async function main() {
  let apiKey = process.env.NEON_API_KEY;
  if (!apiKey) {
    const keyFile = path.resolve('/opt/data/.neon-api-key');
    if (fs.existsSync(keyFile)) {
      apiKey = fs.readFileSync(keyFile, 'utf8').trim();
    }
  }
  if (!apiKey) {
    console.error('ERROR: NEON_API_KEY not set and .neon-api-key not found.');
    process.exit(1);
  }

  const pwUrl = 'https://console.neon.tech/api/v2/projects/raspy-star-93431686/branches/br-wispy-dew-abytefxv/roles/' + ROLE_NAME + '/reveal_password';
  const data = await httpsGet(pwUrl, apiKey);
  const pw = data['password'];
  const connStr = 'postgresql://neondb_owner:' + pw + '@' + HOST + '/neondb?sslmode=require';

  const outputPath = path.resolve(__dirname, '..', 'netlify', 'functions', '_db-config.js');
  const lines = [
    '// Auto-generated by scripts/generate-db-config.js',
    '// prettier-ignore',
    'module.exports = {',
    '  databaseUrl: "' + connStr + '"',
    '};',
    '',
  ];
  fs.writeFileSync(outputPath, lines.join('\n'), 'utf8');
  console.log('✅ _db-config.js written to netlify/functions/');
}

main().catch((err) => {
  console.error('Script failed:', err);
  process.exit(1);
});

/**
 * Build-time script: reads NEON_API_KEY (from env or .neon-api-key),
 * fetches the DB password from Neon's API, and writes db-config.js
 * so the serverless function has the connection string at runtime.
 *
 * Usage: node scripts/generate-db-config.js
 *
 * ENV: NEON_API_KEY (optional — falls back to reading /opt/data/.neon-api-key)
 */

const fs = require('fs');
const path = require('path');

const NEON_API_BASE = 'https://console.neon.tech/api/v2';
const PROJECT_ID = 'raspy-star-93431686';
const BRANCH_ID = 'br-wispy-dew-abytefxv';
const ROLE_NAME = 'neondb_owner';
const HOST = 'ep-old-star-abhpdbpp-pooler.eu-west-2.aws.neon.tech';

const SECRET_PLACEHOLDER = '__DB_SECRET__';

async function main() {
  // Resolve the API key
  let apiKey = process.env.NEON_API_KEY;
  if (!apiKey) {
    const keyFile = path.resolve('/opt/data/.neon-api-key');
    if (fs.existsSync(keyFile)) {
      apiKey = fs.readFileSync(keyFile, 'utf8').trim();
    }
  }
  if (!apiKey) {
    console.error('ERROR: NEON_API_KEY not set and /opt/data/.neon-api-key not found.');
    process.exit(1);
  }

  // Fetch the password from Neon's reveal_password endpoint
  const pwUrl = NEON_API_BASE +
    '/projects/' + PROJECT_ID +
    '/branches/' + BRANCH_ID +
    '/roles/' + ROLE_NAME +
    '/reveal_password';
  const pwRes = await fetch(pwUrl, {
    headers: { Authorization: 'Bearer ' + apiKey, Accept: 'application/json' },
  });

  if (!pwRes.ok) {
    const errText = await pwRes.text();
    console.error('ERROR: Failed to fetch DB password: ' + pwRes.status + ' — ' + errText);
    process.exit(1);
  }

  const data = await pwRes.json();
  const dbSecret = data['password'];

  // Build the connection string with placeholder then replace
  const connStrTemplate = 'postgresql://neondb_owner:' + SECRET_PLACEHOLDER + '@' + HOST + '/neondb?sslmode=require';
  const connStr = connStrTemplate.replace(SECRET_PLACEHOLDER, dbSecret);

  // Write db-config.js to the functions directory
  const outputPath = path.resolve(__dirname, '..', 'netlify', 'functions', 'db-config.js');
  const escaped = connStr.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$/g, '\\$');
  const content = '// Auto-generated by scripts/generate-db-config.js\n' +
    '// prettier-ignore\n' +
    'module.exports = { databaseUrl: "' + escaped + '" };\n';

  fs.writeFileSync(outputPath, content, 'utf8');
  console.log('✅ db-config.js written with connection string inlined');
}

main().catch((err) => {
  console.error('Script failed:', err);
  process.exit(1);
});
